import SwiftUI

extension Font {
    static func nunoRegular(size: CGFloat) -> Font {
        return Font.custom("NunitoSans-12ptExtraLight_Regular", size: size)
    }
    
    static func nunoSemiBold(size: CGFloat) -> Font {
        return Font.custom("NunitoSans-12ptExtraLight_SemiBold", size: size)
    }
}
